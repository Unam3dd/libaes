#include "gf.h"

uint16_t	gf8_mul(uint8_t a, uint8_t b)
{
	uint16_t p = 0;

	while (a && b) {
		
		if (b & 0x1)
			p ^= a;
		
		a = (a & 0x80)
			? (a << 1) ^ 0x11b 
			: (a << 1);
		
		b >>= 1;
	}

	return (p);
}
